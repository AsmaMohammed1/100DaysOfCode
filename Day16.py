# Talk about Tuple
# Fixed size
# use ()
# can't change size or replace values
# can't delete any item in tuple just delete all tuple
# can't add any items

Names = ('Ali','Mohammed','Nasser')
print(Names)
colors = ()
print(colors)
Nums = (6,)
print(Nums)

MyTuple = (7,2,4.5,'red',7.8)
print(MyTuple)

# access tuple items by referring to the index number
print(Names[2])

# print all items
for i in Names:
	print(i)