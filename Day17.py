# Cont'd Talk about Tuple

ages = (23,19,34,27,22,30,22)

# Check if Item Exists
print(19 in ages)

# Repeat Item
print(ages*2)

# To add 2 tuples or more into one tuple

names = ('Bayan','Maha')
print(names+ages)

# Tuple Length
print('Length of ages is: ',len(ages))

# use the tuple() constructor to make a tuple

